function sum(a: number, b: number): number {
    return a + b;
}

function sub(a: number, b: number): number {
    return a - b;
}

function multiply(a: number, b: number): number {
    return a * b;
}

function div(a: number, b: number): number {
    return a / b;
}

export { sum, sub, multiply, div };
